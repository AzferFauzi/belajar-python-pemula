# BELAJAR PENGENLAN DICTIONARY
# dictionary adalah sebuah tipe data yang non immutamble

# cara membuat tipe data dict yaitu dengan cara : data{key:valeu} 

# contoh
data_dict = {
    1:'Ahmad',
    2:'Amara',
    3:'Ananda',
    4:'Audrey',
    5:'Azfer'
}

print(f'Data Dict ke 1 yaitu = {data_dict[1]}')
print(f'Data Dict ke 2 yaitu = {data_dict[2]}')
print(f'Data Dict ke 3 yaitu = {data_dict[3]}')
print(f'Data Dict ke 4 yaitu = {data_dict[4]}')
print(f'Data Dict ke 5 yaitu = {data_dict[5]}')
